package br.com.rafael.exercises.ex09;

public class TestLogicOperators {
    public static void main(String[] args) {
        System.out.println(true && true); //Conjunction (AND)
        System.out.println(false || false); //Disjunction (OR)
        System.out.println(true ^ false); //Exclusive disjunction (XOR)
    }
}
